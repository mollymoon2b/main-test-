#include "libft.h"

char		*ft_strcpy(char *dst, const char *src)
{
	size_t	c;

	c = 0;
	while (src[c])
	{
		dst[c] = src[c];
		c++;
	}
	dst[c] = '\0';
	return (dst);
}
